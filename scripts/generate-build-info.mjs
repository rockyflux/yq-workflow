import { mkdir, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

function formatBuildDate(date) {
  const year = date.getFullYear()
  const month = String(date.getMonth() + 1).padStart(2, '0')
  const day = String(date.getDate()).padStart(2, '0')
  const hours = String(date.getHours()).padStart(2, '0')
  const minutes = String(date.getMinutes()).padStart(2, '0')

  return `${year}-${month}-${day} ${hours}:${minutes}`
}

const scriptDir = dirname(fileURLToPath(import.meta.url))
const projectRoot = dirname(scriptDir)
const outputFile = join(projectRoot, 'src', 'generated', 'build-info.ts')
const buildDate = formatBuildDate(new Date())
const content = `// Auto-generated by scripts/generate-build-info.mjs\nexport const buildDate = '${buildDate}'\n`

await mkdir(dirname(outputFile), { recursive: true })
await writeFile(outputFile, content, 'utf8')
